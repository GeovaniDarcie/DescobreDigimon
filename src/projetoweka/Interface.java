    public void pythonScript(String digimon) {
        try {
            Process exec = Runtime.getRuntime().exec( "python buscaDigimon.py " + digimon);
             
            InputStream in = exec.getErrorStream();
            Scanner scan = new Scanner(in);
 
            while( scan.hasNext() ) {
                System.out.println( scan.nextLine());
               }
     
        } catch (IOException e) {
            e.printStackTrace();
        }
    }

